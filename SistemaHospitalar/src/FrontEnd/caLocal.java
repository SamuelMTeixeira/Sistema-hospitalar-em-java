    private void setShowOpcoes (String tipo) {
        switch (tipo) {
            case "nenhum":
                txtEstado.setVisible(false);
                cbEstado.setVisible(false);

                txtCidade.setVisible(false);
                cbCidade.setVisible(false);

                tfNome.setEditable(false);
                    break;
                    
            case "estado":
                txtEstado.setVisible(false);
                cbEstado.setVisible(false);

                txtCidade.setVisible(false);
                cbCidade.setVisible(false);

                tfNome.setEditable(true);
                    break;
                    
            case "cidade":
                txtEstado.setVisible(true);
                cbEstado.setVisible(true);

                txtCidade.setVisible(false);
                cbCidade.setVisible(false);

                tfNome.setEditable(true);
                    break;    
                    
            case "bairro":
                txtEstado.setVisible(true);
                cbEstado.setVisible(true);

                txtCidade.setVisible(true);
                cbCidade.setVisible(true);

                tfNome.setEditable(true);
                    break;      
        }
    
    
    }

