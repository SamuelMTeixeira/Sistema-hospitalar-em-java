    private void pnlMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlMenu1MouseClicked
    boolean isWrong = false;
            
    /* <<< SE O NOME NÃO FOR INFORMADO EXIBE MENSAGEM DE ERRO >>> */
        if  (cbTipoLocal.getSelectedIndex() == -1) {
            msg.texto("Nenhuma opção foi selecionada");
            cbTipoLocal.requestFocus();
            isWrong = true;
        }
        else if(tfNome.getText().equals("Informe o nome do local...")  || tfNome.getText().equals("")) {
            msg.erro("nome do local", "O campo não pode ficar vazio", tfNome);
            tfNome.setForeground(Color.black);
            isWrong = true;
        }   
    /* <<< SE OS COMBOBOX NÃO FOREM INFORMADOS EXIBE MENSAGEM DE ERRO >>> */
        else if((cbTipoLocal.getSelectedIndex() == 1 || cbTipoLocal.getSelectedIndex() == 2 ) && (cbEstado.getSelectedIndex() == -1 )) {
            msg.texto("Nenhum estado foi selecionado");
            isWrong = true;
        }
    /* <<< SE OS COMBOBOX NÃO FOREM INFORMADOS EXIBE MENSAGEM DE ERRO >>> */
        else if((cbTipoLocal.getSelectedIndex() == 2) && (cbCidade.getSelectedIndex() == -1 ) ) {
            msg.texto("Nenhuma cidade foi selecionado");
            cbCidade.requestFocus();
            isWrong = true;
        }   
        
 
    /* <<< SE ESTIVER TUDO CERTO REALIZA O CADASTRO >>> */
        if(isWrong == false) {
            String SQL= "nulo";
            
            switch (cbTipoLocal.getSelectedIndex()) {
                
                /*<<< CADASTRA UM ESTADO >>> */ 
                case 0:
                    SQL = "INSERT INTO estado (nome) VALUES ('"+tfNome.getText()+"')";
                    break;
                    
               /*<<< CADASTRA UMA CIDADE >>> */ 
                case 1:         
                    SQL = "INSERT INTO cidade (nome, codEstado) VALUES ('"+tfNome.getText()+"','"+db.getCodigo(cbEstado, "codEstado", "estado")+"')";
                    break;
                    
                /*<<< CADASTRA UM BAIRRO >>> */ 
                case 2: 
                    SQL = "INSERT INTO bairro (nome, codCidade) VALUES ('"+tfNome.getText()+"','"+db.getCodigo(cbCidade, "codCidade", "cidade")+"')";
                    break;
            }

            if(!SQL.equals("nulo"))
                this.conexao.SQLExecute(SQL);
            
            msg.successfullyRegistered("Cadastro concluído");
            this.dispose();
        }


        
    }//GEN-LAST:event_pnlMenu1MouseClicked

